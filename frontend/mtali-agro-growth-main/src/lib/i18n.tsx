const getInitialLang = (): Lang => {
  if (typeof window === "undefined") return "en";
  return window.localStorage.getItem(STORAGE_KEY) === "sw" ? "sw" : "en";
};

const I18nContext = createContext<{ lang: Lang; setLang: (l: Lang) => void; t: (k: string) => string }>({
  lang: "en",
  setLang: () => {},
  t: (k) => k,
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(getInitialLang);

  useEffect(() => {
    if (typeof document !== "undefined") document.documentElement.lang = lang === "sw" ? "sw" : "en";
    if (typeof window !== "undefined") window.localStorage.setItem(STORAGE_KEY, lang);
  }, [lang]);

  const value = useMemo(
    () => ({
      lang,
      setLang: setLangState,
      t: (k: string) => dict[k]?.[lang] ?? k,
    }),
    [lang],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export const useI18n = () => useContext(I18nContext);

export const translateCategory = (category: string, lang: Lang) => {
  const key = `category.${category.toLowerCase()}`;
  return dict[key]?.[lang] ?? category;
};

